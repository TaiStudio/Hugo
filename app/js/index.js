const $ = require('jquery');
const { ipcRenderer } = require('electron');

var always = true,
    start = false;

$('#script').on('change', () => {
    ipcRenderer.send('script', $('#script').val());
});

$('#alwaystop').on('click', () => {
    always = !always;
    ipcRenderer.send('always', always);
});

$('#start').on('click', () => {
    start = !start;
    ipcRenderer.send('start', start, `${$('#scroll').val()}`);
});