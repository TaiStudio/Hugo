const $ = require('jquery');
var position = 1,
    speed = 1000,
    interval = null;

function starting(arg, arg1){
    if(arg1 != null){
        speed = arg1;
    }
    if(arg == "true"){
        position = 0;
        $('p').attr('style', `transform: translateY(0%)`);
        interval = setInterval(() => {
            $('p').attr('style', `transform: translateY(-${position}%)`);
            position++;
        }, speed);
    }
    else{
        clearInterval(interval);
    }
}
function script(arg){
    $('p').html(arg);
}